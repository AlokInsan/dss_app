									<p style="text-align: justify;">Dera Sacha Sauda, the socio-spiritual organization was established by most Revered Saint Shah Mastana Ji Maharaj on April 29, 1948 in commandment of HIS Guru honorable Saint Sawan Singh Ji Maharaj. When honorable Guru Ji (Sawan Singh Ji Maharaj) blessed HIS Holiness Shah Mastana Ji with the divine power and ordered HIM to emancipate people in area of Bagar, HE along with some devotees started looking for a site to establish Ashram and then finalized the place where Shah Mastana Ji Dham stands today. The Ashram was then named as Dera Sacha Sauda by revered Guru Ji. This place is situated about two kilometers from the city in the southern direction, on the Sirsa-Bhadra road, which is famously known as Shah Satnam Singh Ji Marg. The place during those times was barren land covered with thorny bushes, but His Holiness chose this piece of land and made it the most auspicious place for the whole world where people from all caste, religion and race come together, without any discrimination and chant the Supreme God’s Name in an equal manner.</p>
									<p>Adorable Shah Mastana Ji with his pious hands dig the first spade on land and laid the auspicious foundation for the ashram. On this occasion, a disciple from Mumbai sung the spiritual rhyme “Hari ki kathaa kahaaniyaan, Gurmeet sunaaiyaan (the stories and legends of the Lord, narrated by the beloved Guru)”, and Mastana Ji expounded on the rhyme. The construction of ashram then began in all earnest and devotees from nearby areas came to be a part of this service. The construction work went on continuously for days and nights. Thorny bushes were uprooted and the earth was leveled at the site. Many snakes, scorpions and other poisonous creatures apparently came out of the earth. His Holiness ordered the volunteers, “No creature should be killed. It should be caught and let off at a distant place.” On the holy command of Guru Ji, no animal was killed and neither did any of the snakes, scorpions or other poisonous creatures harm anyone. The volunteers used to catch the snakes, scorpions, etc.; immediately took them to a place far from human settlements and left them there. This miraculous practice is being followed in Dera Sacha Sauda till date.</p>
									<p>When the construction started, the problem of water was to be solved as the place was on outskirts of Sirsa city. The drinking water used to be brought from the city whereas for construction purpose, water was taken from a stream flowing adjacent to ashram. Bricks were made in ashram itself. At Night, mud bricks were prepared and during the daytime were dried into concrete bricks. Each &amp; every process of construction whether small or big was done under the guidance of His Holiness Mastana Ji Maharaj. His Holiness HIMSELF directed on the architecture of ashram. Initially a hut was constructed, adjoining to which a cave was built with a ceiling equal to the height of the hut. The entrance of the cave was from inside the hut. Even the rooms built were so small that one can only sit there, aiming to help devotees meditate there without lying down. Thorny shrubs were cut and piled into a tall and strong fence around the ashram. Thus, the ashram was ready to be served for human being’s emancipation.</p>
									<p>For naming the ashram then, His Holiness suggested three names: Roohani College (Spiritual College), Chetan Kutiya (Hut of Consciousness), and Sacha Sauda (The True Deal) and later on, with the consent of all present devotees “Dera Sacha Sauda” was finalized. Defining its meaning, Revered Mastana Ji stated that it is not a new religion or new section of the society. It is a place in confluence of all religions where the true deal takes place. Truth in both the worlds is only one that is the Supreme Power, the Almighty and the deal is to chant the God’s Naam (words) in lieu of throwing away all your sins without any cost. HIS Holiness, then firmly stated about Dera Sacha Sauda, “There’s nothing that’s going to diminish here. The Treasure of God’s Name will never end in both the worlds. East will bow here, West will bow here, the rising one will bow here, the descending one will bow here; the whole world will bow.”</p>
									<p>Since, 29th April, 1948 the ashram has seen many changes in its architecture and the count of devotees manifolding into lacs &amp; crores. Present Guru Ji Revered Saint Gurmeet Ram Rahim Singh Ji Insan, in HIS own unique ways, continuously work on beautifying the ashram, besides this, what makes it the most religious and spiritual place in this world is the sense of peace and contentment one feels here.</p>
