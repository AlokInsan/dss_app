									<p style="text-align: justify;"></p>
									<h4 style="text-align: justify;">Men of yore always pondered about philosophical questions like:</h4>
									<p style="text-align: justify;">How did the Universe come into being?<br>
										Does God exist? Why can’t we feel him or at least see him?<br>
										Who am I? How did I come about?<br>
										What happens after death?<br>
										What is the meaning of life?<br>
										Are morals of any relevance?</p>
									<h5 style="text-align: justify;">Such questions have hounded man for ages.</h5>
									<p style="text-align: justify;">And it is to answer these questions, GOD sends HIS messengers in every Yuga aka Era. These messengers come in the form of Saints, Prophets, Gurus, etc. They make man realize his roots and what religion is all about.</p>
									<h4 style="text-align: justify;">Religion: Reverence for GOD</h4>
									<p style="text-align: justify;">Religion deals with man’s obligation towards GOD and forms a meaningful bond between him and GOD. And although you may find this hard to believe, religion or spirituality as we know them today wouldn’t have ever been born had Saints not existed!<br>
										Saints are blessed souls who incarnate on this earth to provide the true knowledge of life to mankind, without asking for anything in return, and certainly not for money. They form the basis of spirituality and religion. And they never disperse facts without trying them first by themselves.<br>
										The answer to all the questions mentioned above is together knit into a collection of texts which are then propagated under the umbrella of a religion or a sect. They come to earth to prove to the man that GOD indeed exists and so that they can make him rise to the level of GOD.<br>
										It is through Saints that what we know as morals and virtue float in society today. Customs too are made by Saints of yore.</p>
									<h4 style="text-align: justify;">Saints are indeed messengers who aim at self-enlightenment and introspection for man.</h4>
									<p style="text-align: justify;">In this Dark Age aka Kal Yuga, evil is at the very peak in society. A Savior of Humanity, His Holiness, Shah Mastana Ji Maharaj incarnated himself, thus, and founded a Socio-Spiritual organization which we today call the Dera Sacha Sauda in Sirsa.</p>
									<h2 style="text-align: justify;">History of Dera Sacha Sauda (Sirsa)</h2>
									<p style="text-align: justify;">Literally means a place of real truth.<br>
										God’s True words or the True Method of Meditation is imparted over here.<br>
										Guru of His Holiness Shah Mastana Ji Maharaj, the Revered Saint Sawan Shah Ji Maharaj when pleased with Mastana Ji’s eternal love for his Satguru, embraced Mastana Ji with the divine power of Spirituality. He sent him to semi-arid Transitional Plains of India known as Bagar area to emancipate people through God’s True Words.<br>
										Mastana Ji was then honored with the title of “Emperor of Bagar” by Revered Sawan Shah Ji Maharaj. Respectfully, He chose Sirsa as the epicenter of Spirituality and put the foundation stone of Dera Sacha Sauda on 29th April 1948.<br>
										His Holiness taught the Method of Meditation for 12 years to thousands of seekers and encouraged them to abandon evil deeds and live an honest living through hard work. Shah Mastana Ji Maharaj also established at least a dozen additional ashrams in the states of Haryana, Uttar Pradesh, Punjab, Rajasthan, Delhi, etc. He tried to shower his divine blessings far and wide.</p>
									<h4 style="text-align: justify;">28th February 1960</h4>
									<p style="text-align: justify;">Revered Shah Mastana Ji Maharaj embodied Himself, the Divine Light, into the body of Adorable Param Pita Shah Satnam Ji Maharaj and empowered Him with divine power to carry the task of salvation of mankind.<br>
										His Holiness Param Pita Ji very devoutly and lovingly watered the seeds of spirituality sowed by Revered Shah Mastana Ji Maharaj and through the sapling grown from it, the fragrance of spirituality was spread in every corner of the world. Adorable Param Pitaji amplified the pace of emancipation and blessed around 11 Lac 08 Thousand 429 Hundred people with Method of Meditation till August 26, 1990.</p>
									<h4 style="text-align: justify;">23rd September 1990</h4>
									<p style="text-align: justify;">His Holiness Param Pita Shah Satnam Ji reincarnated HIMSELF into the human body of present Guru Ji
										<span style="color: #0000ff;">
											<a style="color: #0000ff;" href="https://www.derasachasauda.org/saint-gurmeet-ram-rahim-singh-ji-insan/">
												<span style="text-decoration: underline;">
													<strong> Saint Dr. Gurmeet Ram Rahim Singh Ji Insan</strong></span></a></span> and passed on the same Divine Light to HIM. His Holiness Param Pita Ji while addressing the whole Sadh Sangat, the company of Holy, strongly affirmed, “We were, We are and We will be” clearly mentioning that the same Divine Power which started working with Revered Shah Mastana Ji Maharaj is still working and will keep on working in the form of Hazoor Maharaj Ji for the salvation of mankind.<br>
										Today, the sapling of spirituality which was planted by Shah Mastana Ji Maharaj, nurtured by Param Pitaji, and handed over to Hazoor Maharaj Ji, has grown into a huge tree with over 5 million devotees as branches. Hazoor Maharaj Ji has helped million of people get rid of their vices by teaching them Meditation.</p>
									<h4 style="text-align: justify;">Moreover, Revered Guru Ji is a true humanitarian at heart!</h4>
									<p style="text-align: justify;">He has initiated 134 humanitarian activities for the welfare of all types of living beings including flora, fauna, and humans. According to Him, every living being is a child of GOD, and to serve GOD one must serve HIS Children.<br>
										The miracles which took place through His humanitarian works earned the Dera Sacha Sauda Sirsa the recognition of Guinness Book of World Records (not once, but 18 times) amongst those from several other national and international authorities!<br>
										Adorable Hazoor Maharaj Ji has guided the Dera with His Holy guidance and leadership to previously unknown heights in Spirituality and Humanity.</p>
