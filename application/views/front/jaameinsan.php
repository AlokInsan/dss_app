									<div class="avia-image-overlay-wrap"><img class="avia_image" src="https://www.derasachasauda.org/wp-content/uploads/2014/08/jaam-e-insan-by-pita-ji.jpg" alt="jaam e insan" title="jaam e insan by pita ji" height="361" width="300"></div></div></div>Humanitarianism is dying.The morals and values that were the cornerstone of good conduct are vanishing fast. Religion, caste and color have become reasons for society to erupt into riots.
							<p></p>
							<p>In the face of these challenges, where hate has become the world’s chosen creed, Guru Ji started a custom of the pledge for humanitarianism, called the ‘
								<strong>Nectar of the Humanitarianism</strong>’, this pledge includes partaking a Holy drink made from milk, water and rose essence. The purpose of pledge is to renew the commitment of those who have learnt meditation, to always stay on the path of righteousness and humanitarianism.</p>
							<p>The pledge was started by Guru Ji on
								<strong>29th April 2007</strong>. Millions have partaken the Nectar and dedicating themselves to society as evolved human beings. Thousands of people have reported that their incurable diseases have been cured. HIV cases are on record where viral counts have become undetectable and even congenitally blind patients have started seeing. Guru Ji says, in all humility, “
								<em>The faith and devotion of the meditator can work wonders, as the grace of God, is boundless.</em>”</p>
