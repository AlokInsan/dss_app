					<div style="padding-bottom:10px; " class="av-special-heading av-special-heading-h2    avia-builder-el-2  el_after_av_textblock  el_before_av_textblock   ">
						<h2 class="av-special-heading-tag ">Principles For Jaam-E-Insan</h2>
						<div class="special-heading-border">
							<div class="special-heading-inner-border"></div></div></div>
					<section class="av_textblock_section ">
						<div class="avia_textblock  ">
							<p>
								<strong>Note: This is not a religious conversion but an affirmation to be true to one’s own religion while recognizing the unity of all religions and the universal religion of Humanity.</strong></p>
							<p>1. Use ‘Insan’ after your surname (Sharma, Verma, Arora, Sandhu etc.) or preferably use only ‘Insan’ as your surname.</p>
							<p>2. Do not consume alcohol.</p>
							<p>3. Do not consume non-vegetarian food.</p>
							<p>4. Look at other men and women, depending on their ages, if they are older, as your father or mother, if they are of your own age, as your brother or sister, and if they are younger than you, as your son or daughter.</p>
							<p>5. At least one day out of a week or six hours should be spent in selfless service, or 4-5 days in a month.</p>
							<p>6. Upon waking up in the morning, touch the feet of your elders and say the Naara and bless the children with love and say the Naara.</p>
							<p>7. Inspire people to give up their bad habits and help them receive the Naam/Gurumantra from Guruji, as Guruji has said that this act of putting others on the path to God results in as much spiritual merit as is gained by donating a hundred cows.</p>
							<p>8. Do not earn your livelihood through dishonesty, theft, robbery, bribery or corruption. Earn your living through hard work and rightful means.</p>
							<p>9. A day should begin with meditation and a prayer to God that you be able to do good deeds, that you be good to all, and that you be able to realize Him.</p>
							<p>10. Meditate and pray to God before eating anything so that God gives you spiritual health and freshness.</p>
							<p>11. Use a fifteenth of your earnings in selfless service and the welfare of mankind. Donate with your own hands or donate to Shah Satnam Ji Green S Welfare Force Wing so that the poor and the needy can be helped.</p>
							<p>12. If you wish to practice charity, donate blood, impart education, pledge to donate your eyes or your whole body after death, and spend money for the welfare of mankind.</p>
							<p>13. It is necessary that you be honest and fair in your worldly dealings.</p>
							<p>14. Do not discriminate among people on the basis of caste.</p>
							<p>15. Avoid all kinds of intoxicants; include physical exercise in your daily routine; and as much as possible do all your work yourselves.</p>
							<p>16. Do not lend money at high interest rates and do not exploit the poor and helpless.</p>
							<p>17. Do not use abusive language and always speak politely and courteously.</p>
							<p>18. It is necessary that you be truthful in thought, word and deed.</p>
							<p>19. Give up gossip, backbiting and jealousy, and inspire people towards God.</p>
							<p>20. Adopt humility, modesty and simplicity.</p>
							<p>21. Abandon social evils like dowry system, child marriage, foeticide, child labour, etc., and build a healthy society. Note that the wealth given to the bride by her guardians out of their own will is not dowry but the girl’s right.</p>
							<p>22. Consider girls and boys as equal and give them equal rights.</p>
							<p>23. When you come across someone who is in trouble or has had an accident, do whatever is possible to help.</p>
							<p>24. Do not give alms to beggars who are healthy and inspire them to earn their living through hard work.</p>
							<p>25. Provide food for the hungry, water for the thirsty, help for the helpless, education for the illiterate, and if possible employment for the unemployed.</p>
							<p>26. Do not hurt others’ religious sentiments.</p>
							<p>27. You shall not listen to ill-spoken words against your Guru, nor shall you involve yourself in arguments or speak ill of others.</p>
							<p>28. Avoiding bad company, always stay in the company of those who talk of God and goodness.</p>
							<p>29. Walk on the path of truth with honor, faith and determination.</p>
							<p>30. You shall not do injustice to others nor will you bear injustice.</p>
							<p>31. The welfare of others is your religion and humanity is your caste.</p>
							<p>32. Create a daily routine of meditation and meditate at least for half an hour each in the morning and evening.</p>
							<p>33. If possible meditate in the morning for half an hour or an hour between 2 and 5 a.m., have your breakfast after that, earn your living through hard work, spend time with your family, meditate for half an hour or an hour before you sleep, and go to sleep on time.</p>
							<p>34. Always wear the pendant so that you remember that we are all one and God is also one.</p>
							<p>35. You shall be ready to sacrifice your life to protect the honour of your motherland and your Guru.</p>
							<p>36. Practice nonviolence. Neither shall you kill nor shall you torment other living beings.</p>
							<p>37. A pregnant woman should read, watch and listen to the scriptures, hymns and spiritual discourses of her religion and to the legends of brave warriors. This will have a very positive effect on the life of the yet-to-be-born child.</p>
							<p>38. Listen to or watch the cassettes or CDs of Satsangs or Bhajans while having your food.</p>
							<p>39. Avoid obscenity and vulgarity on TV, movies, magazines, etc.</p>
							<p>40. Plant trees and saplings and take care of them so that your environment becomes clean.</p>
							<p>41. Women who become widows at a young age should be helped to remarry. If you are unable to help them remarry, inform the department at Room No. 50, Dera Sacha Sauda</p>
							<p>42. Stay away from superstitions such as ghosts and spirits, black magic, expenditure on post-death rituals, and fake saints.</p>
							<p>43. Be aware and make others aware of social evils and ills.</p>
							<p>44. As you grow old you should hand over the responsibility of your family to the next generation and spend your time in devotion to God.</p>
							<p>45. If you achieve something, do not become egoistic. If you desire for something, work honestly and diligently for it, meditate, and avoid anxiety.</p>
							<p>46. Whenever you are angry, say the Naara, drink some cold water, and meditate for five minutes. As much as possible, stay away from anger.</p>
							<p>47. Love all of God’s creations in a selfless manner.</p>
