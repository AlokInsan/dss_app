								<h3>The Life of His Holiness Shah Satnam Singh Ji Maharaj</h3>
								<div class="avia-image-container avia_animated_image avia_animate_when_almost_visible left-to-right av-styling-    avia-builder-el-0  avia-builder-el-no-sibling  avia-align-left  avia_start_animation avia_start_delayed_animation">
									</div>
								<p style="text-align: justify;">
									<strong>Birth Name:</strong> Shri Harbans Singh<br>
									<strong>Incarnated</strong> on 25th January 1919<br>
									<strong>In</strong> the Holy Village of Sri Jalalana Sahib in the district of Sirsa (Haryana in India)<br>
									<strong>Respected Father:</strong> Shri Wareyam Singh Ji<br>
									<strong>Revered Mother:</strong> Aas Kaur Ji<br>
									Both parents were learned and pious souls and belonged to a Sindhu family. His Holiness was the sole child of his parents.</p>
								<p>
									<strong>His Holiness grew up to be an eminent landlord of the village.</strong><br>
									His Holiness used to love God as a child and wanted to become one with him from a very tender age. This ambition leads him to start the journey of finding his perfect spiritual master who would fulfill his desire of achieving God.<br>
									During this quest for Nirvana, His Holiness met several people who were great devotees of God. He also listened to their teachings and practiced what they taught. Unfortunately, he wasn’t successful in his quest so quickly.</p>
								<p>
									<strong>Finally, His Holiness got the chance to attend a spiritual discourse of Shah Mastana Ji Maharaj and followed next was his pious Darshan.</strong><br>
									His Holiness was impressed by Shah Mastana Ji and felt that he could perhaps be his perfect spiritual Master. The One he was searching for. And he tried to follow this revered saint with utmost sincerity and devotion. He also started attending Satsangs (spiritual congregations) of Shah Mastana Ji. This process took place for three consecutive years. During the same time, he selflessly served his fellow disciples.<br>
									What was surprising was that whenever His Holiness expressed the desire to learn meditation from his Master, he would be refused with the reply, “When the timing is right, I will call you on my own and will bless you with the Naam. Kaal (Time or Negative energy) will not harm you in any manner till that time. You can keep attending the Satsangs.”</p>
								<p>
									<strong>And the day came: 14th March 1954</strong><br>
									Shah Mastana Ji conducted a Satsang (spiritual discourse) in the village of Ghukeyanwali near Sirsa. And at the completion of the Satsang, Shah Mastana Ji Maharaj called Shah Satnam Singh Ji Maharaj and said, “Come inside and sit near the chair. Tonight the order from Almighty has come to teach you Meditation (Naam).”<br>
									Making Shah Satnam Ji sit by his side, Shah Mastana Ji said, “I have called you for Meditation (Naam) and have made you sit by my side as I have to entrust you with a very special responsibility. You will be that leader of “
									<em>Zindaram</em>” (Spirituality) who will shower the blessings of the Naam (Meditation) on the world.”</p>
								<blockquote>
									<p>“Sant bachan palte nahin, palat jaye brahamand”</p></blockquote>
								<p>Words uttered by a Perfect Saint are reflected in reality, in the Universe. There is no way to change the words once they are uttered. Even if that requires the law of the Universe to change, so be it, but His words never fail.</p>
								<p>
									<strong>28th February 1960</strong><br>
									Finally, Shah Mastana Ji Maharaj revealed this secret to the world.<br>
									<strong>And why he came to be called ‘Shah Satnam Singh Ji’</strong><br>
									Shah Mastana Ji Maharaj who made His Holiness the successor to the Dera Sacha Sauda Sirsa blessed him with the Spiritual Wealth and honored him by the name of ‘Shah Satnam Singh Ji’.<br>
									Shah Mastana Ji said I tested Satnam Singh Ji in every manner without him being aware of it. Shah Satnam Singh Ji had to demolish his house and bear humiliation, to learn the Method of Meditation (the Holy Naam).” He added, “Today, I have transformed Sardar Satnam Singh Ji from an ordinary person to the Supreme Being.”<br>
									He further explained why Shah Mastana Ji chose the name ‘Satnam’ for his successor. He said, “Satnam is the power which controls the Universe. It is the supporting power due to which the world exists.”<br>
									Curious Shah Mastana Ji expressed a doubt. He asked himself, “Only a part of the world’s population believes in Saints and others pay no attention to them. How will they get salvation if their souls are gripped in the cycle of birth and death (Cycle of Transmigration)? Later, Shah Mastana Ji Maharaj added, “The soul of any person who utters ‘Satnam’ or who has His (Shah Satnam Singh Ji’s) pious Darshan will never go to Hell.”<br>
									His Holiness, in front of a massive public gathering in the Dera Sacha Sauda, openly declared Shah Satnam Singh Ji as his Spiritual Successor. For this purpose, Shah Mastana Ji also carried out a mass procession in the streets of Sirsa. His Holiness did all this to make the people of Sirsa aware that Sardar Sahib Ji of Jalalana was the new spiritual head of the Dera Sacha Sauda.</p>
								<p>
									<strong>Shah Mastana Ji left for his heavenly abode on 18th April 1960. Followed next, Social Service!</strong><br>
									Taking charge of the Dera, Shah Satnam Singh Ji built the building of the Dera Sacha Sauda Sirsa on the foundation laid by Shah Mastana Ji.<br>
									His Holiness expanded the ashrams built by Shah Mastana Ji and built a new ashram in Barnava near Meerut (Uttar Pradesh). He also wrote more than 20 books and 1500 hymns in different languages. People have found these to be very impressive and yet simple to understand.<br>
									As per the sweet will of Shah Mastana Ji, Shah Satnam Ji started an exemplary system of marriages in the Ashram. In this system, marriages are held in a very simple manner without any superstition, custom, extravagance nor dowry. This system is still practiced.</p>
								<p>
									<strong>3 years after Mastana Ji Maharaj’s departure…1963</strong><br>
									Param Pita Shah Satnam Singh Ji started to give Naam to people. His Holiness carried out this assigned task with utmost dedication for over 27 years, from 1963 to 1990. He also showered unforgettable blessings and love upon his followers. Devotees consider themselves indebted to him for his love and kindness.<br>
									His Holiness held Spiritual discourses (Satsangs) across
									<strong>2250 villages</strong> and many cities of Punjab, Haryana, Rajasthan, and Uttar Pradesh.<br>
									His Holiness initiated
									<strong>11,08,429 souls</strong> on the path of God-realization in a period of 27 years and 4 months. He made lakhs of people realize the presence of God by teaching them the Method of Meditation. This inspired lakhs of people to give up non-vegetarian food, alcohol, and illicit sex.</p>
								<p>
									<strong>The desire to retire…1989</strong><br>
									Param Pita Shah Satnam Singh Ji expressed his desire to hand over the reign to a deserving successor. For this, he regularly met and consulted his disciples for about a year and two months. This was done so that the authenticity of the successor isn’t doubted later.<br>
									His Holiness declared, “I don’t want any confusion or infringement to arise regarding the successor as has happened earlier. Thus, I will appoint my successor in a grand and magnificent manner like it hasn’t been seen before, nor will it be seen ever again!”</p>
								<p>
									<strong>The Successor…23rd September 1990</strong><br>
									A formal ceremony was held in Dera Sacha Sauda. A huge following of devotees were present. And His Holiness appointed and declared Saint Dr. Gurmeet Ram Rahim Singh Ji Insan as his successor. He also declared, “Divine blessings have been showered on Saint Dr. Gurmeet Ram Rahim Singh Ji Insan as per the order of Shah Mastana Ji Maharaj. It was the will of the Lord and so it has happened. I have appointed Saint Dr. Gurmeet Ram Rahim Singh Ji Insan as my successor.”<br>
									Param Pita Shah Satnam Singh Ji also stated, “Any person who loves Sant Ji ( Saint Dr. Gurmeet Ram Rahim Singh Ji Insan), loves me. Anybody who obeys him obeys me. A person who trusts Him trusts me. If anybody has ill feelings for him, he has the same for me.”<br>
									Such spiritual wealth is never bestowed based on false show off. The person to whom this spiritual wealth is granted is an enlightened soul right from his very birth. The Satguru make Him and others realize of His enlightenment. Satguru showers his blessings to his successor and makes him follow a life which is beyond the imagination of a common man.</p>
								<blockquote>
									<p>“Wich sharabe rang musalla, je Murshid farmave, Wakifkar kadimi hunda, galti kadi na khave”</p></blockquote>
								<p>Words of a Muslim Saint state that a disciple must always follow the orders of his Satguru for He can never be wrong.<br>
									In this context, it means, that the Satguru himself knows the secret regarding his successor which he discloses at the appropriate time. His order should be complied with.</p>
								<p>
									<strong>History repeats itself</strong><br>
									This progression was similar to the one where Shehanshah Mastana Ji handed over the reign to his successor. At that point in time, people didn’t understand this. Later on, they accepted it. This happened with the kindness and blessings of Mastana Ji Maharaj.<br>
									No power in the world could affect the blessings that Shah Mastana Ji bestowed upon Shah Satnam Singh Ji.</p>
								<p>
									<strong>Hukumnama (The Order) of Param Pita Shah Satnam Singh Ji Maharaj (23rd September 1990)</strong><br>
									If a person complies with the order of his Satguru, he will always be happy. Negative thoughts (Mann) may try to mislead him. However, a devotee who remembers His Satguru and follows his orders will be able to save himself from the negative moves and the Satguru will always be present with him.</p>
								<p>With this ceremony, Param Pita Shah Satnam Singh Ji handed over the charge of the Dera and its followers to Revered Saint Dr. Gurmeet Ram Rahim Singh Ji Insan and declared, “I am now free from the responsibilities of the Dera. Now my duty is only to eat, sleep and pray. Saint Ji (Saint Dr. Gurmeet Ram Rahim Singh Ji Insan) will take care of all the affairs of the Dera. Any person who wishes to talk to me must talk to Saint Ji. I will only watch the devotees come.”<br>
									The Supreme Being incarnates on this planet in the form of a true Spiritual Saint to show mankind the right path which leads to a soul’s actual goal, ie. achieving salvation from the vicious cycle of Birth and Death (cycle of Transmigration) by realizing God.<br>
									Param Pita Shah Satnam Singh Ji was Spiritually Adept and had incarnated in this world for this very purpose.<br>
									His Holiness performed his duty very successfully, passed on his spiritual wealth to his successor and went back to his original home – the Home of the Lord on 13th December 1991.<br>
									<strong>Dhan Dhan Satguru Tera Hi Asra!</strong></p>
