						<div class="avia_textblock  ">
							<div style="font-size: 15px; line-height: 1.6em;">
								<strong>1.</strong>&nbsp;In Dera Sacha Sauda all religions are equally honoured and welcomed.<br>
								<strong>2.</strong> Dera Sacha Sauda believes in humanity as the greatest religion and is involved in the true service of humanity. The poor, helpless and sick are helped here in every possible manner.<br>
								<strong>3. </strong>In Dera Sacha Sauda no discrimination is made among people on the basis of rich and poor, inferior and superior, etc.<br>
								<strong>4.</strong> Dera Sacha Sauda does not accept any offerings, donations or fees for the meditation that is taught here.<br>
								<strong>5.</strong> Dera Sacha Sauda rejects the false customs, malpractices, superficiality and deceit that happen in the name of religion.<br>
								<strong>6.</strong> The Truth is taught at Dera Sacha Sauda and inspiration given to walk on the path of Truth. Prabhu, Paramatma, Ram, Allah, Waheguru, God is the only Truth and the method of meditating on Him is taught here. One need not change one’s religion or outward appearance in order to practice this meditation.<br>
								<strong>7.</strong> Dera Sacha Sauda is an assembly of pure spirituality and only the method of connecting man with God is taught here.<br>
								<strong>8.</strong> Dera Sacha Sauda does not have any links with politics.<br>
								<strong>9.</strong> The assembly of Dera Sacha Sauda is decorated with the virtues of humility, modesty, simplicity and truthfulness. Only he who adopts these virtues is worthy of praise.<br>
								<strong>10.</strong> Consumption of alcohol and other intoxicants is strictly prohibited in Dera Sacha Sauda.<br>
								<strong>11.</strong> The cardinal rules of DSS are to not eat non-vegetarian food, to not consume alcohol, and for men and women to consider persons other than their spouses as either mother, sister or daughter, or father, brother or son.<br>
								<strong>12.</strong> DSS strongly opposes customs like the dowry system and other social ills. It discourages the superficial rituals and unreasonable expenditure related to births, deaths and marriages. At the Satsangs conducted by DSS, marriages are conducted in the presence of the Sadh-Sangat in a simple manner by exchanging garlands.<br>
								<strong>13.</strong> DSS believes in earning one’s livelihood through hard work and rightful means. The celibate volunteers of the Dera work for 15 to 18 hours a day and they practice meditation. It is only by earning one’s bread through rightful means that one gets peace of mind and makes rapid progress in spirituality.<br>
								<strong>14.</strong> DSS strictly opposes theft, dishonesty, bribery and corruption. People here are inspired to walk on the path of truth and honesty and serve humanity as this is necessary in today’s age.<br>
								<strong>15.</strong> At the Satsangs conducted by DSS separate arrangements are made for men and women to sit, dine and sleep. The arrangements for men are taken care of by men and those for women by women.Whoever abides by the above sacred rules of DSS and meditates on Allah, Ram, Waheguru, God, he/she will be blessed with the happiness of both worlds, he/she will find God, and his/her soul will attain to salvation.</div>
