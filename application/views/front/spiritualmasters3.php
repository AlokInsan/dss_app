									<h5 style="text-align: justify;">The Life of Revered
										<span style="text-decoration: underline; color: #0000ff;">
											<a style="color: #0000ff; text-decoration: underline;" href="https://www.saintgurmeetramrahimsinghjiinsan.org/">Saint Dr. Gurmeet Ram Rahim Singh Ji Insan</a></span></h5>
									<p style="text-align: justify;">
										<strong>Date of Birth:</strong> 15th August 1967<br>
										<strong>Place of Birth:</strong> The Holy village of Sri Gurusar Modia (District: Sri Ganganagar, State: Rajasthan, Country: India)</p>
									<p style="text-align: justify;">
										<strong>Revered Father:</strong> Sardar Magghar Singh Ji<br>
										A landlord and the head of his village.</p>
									<p style="text-align: justify;">
										<strong>Respected Mother:</strong> Mata Naseeb Kaur Ji<br>
										A dedicated devotee of God.</p>
									<h4 style="text-align: justify;">The Prophecy of January 1960</h4>
									<p style="text-align: justify;">Shah Mastana Ji in his last Satsang (spiritual discourse) of January 1960 had repeatedly prophesized, “Exactly after seven years, I will come again in the form of the third body (the Third Master)”.</p>
									<p style="text-align: justify;">Often people take the words of saints lightly. It is only when they come true do they realize the significance of those Vachans (words/promises).</p>
									<p style="text-align: justify;">And the same happened with Shah Mastana Ji as well. His disciples didn’t understand the significance of his words. Only when Revered Saint Dr. Gurmeet Ram Rahim Singh Ji Insan incarnated on 15th August 1967, exactly after the completion of the said seven years, did the disciples understand his prophecy.</p>
									<h4 style="text-align: justify;">Childhood of His Holiness |&nbsp;Saint Dr. Gurmeet Ram Rahim Singh Ji Insan</h4>
									<p style="text-align: justify;">Being the only child of his parents, he was the favorite of everybody in his family.</p>
									<p style="text-align: justify;">From a very tender age, his deeds hinted at the fact that he wasn’t an ordinary child. Soon, his parents realized that he wasn’t merely a child but the image of the Lord. And thus, they never hurt him physically or verbally.</p>
									<p style="text-align: justify;">The natives of the land also learned about this special child.</p>
									<p style="text-align: justify;">His Holiness was always kind and helping the child. He used to help the poor and needy financially and in every way possible for him. These qualities he carried into his adulthood and are reflected in his personality even today.</p>
									<p style="text-align: justify;">During years of his education, His Holiness performed excellently in academics, sports, and co-curricular activities. He also did whatever he did with great dedication and confidence.</p>
									<p style="text-align: justify;">At just eight years of age, Revered Pitaji started driving a tractor in his father’s agricultural fields.</p>
									<h4 style="text-align: justify;">Initiation – 31st March 1974</h4>
									<p style="text-align: justify;">At a very young age of just seven, His Holiness was bestowed with the Naam from Param Pita Shah Satnam Singh Ji Maharaj at Dera Sacha Sauda, Sirsa. And this was how one Master Saint found the next in the lineage.</p>
									<p style="text-align: justify;">Revered Pitaji enthusiastically motivated his friends also to follow suit.</p>
									<h4 style="text-align: justify;">The Secret – 23rd September 1990</h4>
									<p style="text-align: justify;">Param Pita Shah Satnam Singh Ji Maharaj invited his disciples from all over the country and conducted a grand Satsang where His Holiness declared Revered Saint Dr. Gurmeet Ram Rahim Singh Ji Insan&nbsp;as the successor to the divine throne.</p>
									<p style="text-align: justify;">In this formal ceremony, Shah Satnam Singh Ji Maharaj bestowed spiritual wealth to Pitaji and handed him the charge of the Dera.</p>
									<p style="text-align: justify;">This date is not only a milestone in the history of the Dera Sacha Sauda but also an important date for all mankind. It was on this date, that Revered Pitaji left his family at a young age of 23 and sacrificed himself to the cause of humanity.</p>
									<p style="text-align: justify;">Since then Revered Pitaji has worked tirelessly to spread the divine message and blessings of our Param Guru, throughout the world.</p>
									<h4 style="text-align: justify;">Revered Saint Dr. Gurmeet Ram Rahim Singh Ji Insan</h4>
									<p style="text-align: justify;">Saint Dr. Gurmeet Ram Rahim Singh Ji Insan drove over 60 million souls via the Naam/Gurumantra, on the path of God-realization.</p>
									<p style="text-align: justify;">He can be credited with the fact that he has transformed several atheists into disciples of spirituality. His Holiness has also contributed to motivating millions of people to perform selfless service to mankind.</p>
									<p style="text-align: justify;">Revered Pitaji has helped millions of people understand the presence and importance of God and how he is present within all of us, and not on mountains or forests as is conventionally taught. This is indeed a big landmark in the history of mankind.</p>
									<h4 style="text-align: justify;">Quotes by Saint Dr. Gurmeet Ram Rahim Singh Ji Insan</h4>
									<p style="text-align: justify;">“Anybody who remembers Param Pita Shah Satnam Singh Ji Maharaj by meditating on the Naam-Shabad with a pure heart will definitely be honored by the pious vision, ie. the live presence of Param Pita Shah Satnam Singh Ji Maharaj.”</p>
									<p style="text-align: justify;">“If a disciple makes a request for the fulfillment of a reasonable desire to the Satguru by meditating in the Braham Moohrat, ie. 2-5 AM, that request will directly reach the Satguru and will definitely be accepted by him.”</p>
									<blockquote>
										<p>“Na hum kiya na kar saken, na hi hum kuch jaanat, Jo kiya karenge Shah Satnam Ji, bhayo Sant hi Sant”.</p></blockquote>
									<p style="text-align: justify;">Which means, “Neither have I done anything nor can I ever do anything? It is Shah Satnam Ji Maharaj who does everything and who will do whatever is done in the future.”</p>
									<h4 style="text-align: justify;">Welfare works undertaken by Saint Dr. Gurmeet Ram Rahim Singh Ji Insan</h4>
									<p style="text-align: justify;">His Holiness has built more than a dozen ashrams in the states of:</p>
									<ul style="text-align: justify;">
										<li>Gujarat</li>
										<li>Maharashtra</li>
										<li>Himachal Pradesh</li>
										<li>Punjab</li>
										<li>Haryana</li>
										<li>Madhya Pradesh</li>
										<li>Chhattisgarh</li>
										<li>Rajasthan</li>
										<li>Delhi</li>
										<li>Chandigarh</li>
									</ul>
									<p style="text-align: justify;">Revered Pitaji lives a disciplined life to perform extra-ordinary hard work because of his supreme love for humanity. His busy schedule is a witness to this fact. His life proves below proverbs to be true:<br>
										‘Service to mankind is service to God’<br>
										‘Love is God and God is Love’</p>
									<p style="text-align: justify;">Revered Pitaji has also composed hundreds of hymns and has written several books.</p>
									<p style="text-align: justify;">His Holiness is the Savior of Humanity initiated 134 activities of social welfare for the well-being of nature as well as mankind. (
										<strong>
											<span style="color: #0000ff;">
												<a style="color: #0000ff;" title="http://derasachasauda.org/humanitarian-works/" href="http://derasachasauda.org/humanitarian-works/">http://derasachasauda.org/humanitarian-works/</a></span></strong>)</p>
									<p style="text-align: justify;">In order to facilitate the execution of these humanitarian activities smoothly and successfully, His Holiness divided the states into functional blocks. Each block constitutes of about 30 villages (approximately five to ten thousand followers). And in every block, there is a committee of 15 responsible persons each of whom is known as a “15 members”. In their respective blocks, these teams manage welfare and humanitarian projects at their level including providing free medical treatment to the needy, constructing houses for the poor, helping financially in marriages of destitute girls, donating organs including blood and eyes after death, solving cases of disputes without litigation, etc.</p>
									<p style="text-align: justify;">At the state level, His Holiness has organized a team of 45 people per state who supervise all the blocks in their state and settle disputes which the 15 members failed to resolve.</p>
									<h4 style="text-align: justify;">Disaster Relief and Humanitarian Work</h4>
									<p style="text-align: justify;">For this purpose, Revered Pitaji has formed a special welfare team ‘Shah Satnam Ji Green ‘S’ Welfare Force Wing’.<br>
										Over 70,000 followers are already members of this force.</p>
									<p style="text-align: justify;">Inspired by Revered Pitaji, these members have vowed to serve humanity during natural disasters, regardless of which part of the world they need to go to.</p>
									<p style="text-align: justify;">Shah Satnam Ji Green ‘S’ Welfare Force Wing is an active organization and ceaselessly tries to help humanity for the better.</p>
									<h4 style="text-align: justify;">The Naam (Method of Meditation)</h4>
									<p style="text-align: justify;">Revered Guru Ji (Saint Dr. Gurmeet Ram Rahim Singh Ji Insan), the chief of
										<a href="https://www.derasachasauda.org/">
											<span style="text-decoration: underline; color: #0000ff;">
												<strong>Dera Sacha Sauda</strong></span></a>, teaches the method of meditation to disciples from all parts of the world including (but not limited to):<br>
										USA, Canada, UAE, Australia, UK, Etc…</p>
									<p style="text-align: justify;">It is only because of the Revered Guru Ji that the Dera is flourishing the way it is today.</p>
