										<h2>The Environment:</h2>
									</div>
									<p style="text-align: justify;">
										<strong>Conservation of Fossil fuel:</strong> It takes 78 calories of fossil fuel to produce 1 calorie of beef protein; 35 calories for 1 calorie of pork; 22 calories for 1 of poultry; but just 1 calorie of fossil fuel for 1 calorie of soybeans. Eating plant foods instead of animal foods helps in conserving our non-renewable sources of energy.<br>
										<strong>Water Conservation:</strong>
										<span style="font-size: 10.0pt; font-family: 'Helvetica','sans-serif'; color: #3c3c3c;">It takes 3 to 15 times as much water to produce animal protein as it does for plant protein. As a vegetarian, one contributes to water conservation.</span></p>
									<p style="text-align: justify;">
										<strong>Efficient use of grains:</strong> It takes up to 16 pounds of soybeans and grains to produce 1 lb. of beef and 3 to 6 lbs. to produce 1 lb of turkey &amp; egg. Eating grain foods directly makes the food supply more efficient &amp; contributes to the environment.</p>
									<p style="text-align: justify;">
										<strong>Soil conservation:</strong> When grains &amp; legumes are used more efficiently, our precious topsoil is automatically made more efficient in its use. We use less agricultural resources to provide for the same number of people.<br>
										<strong>Saving our forests:</strong> Tropical forests and other tropic regions are destroyed daily, in part, to create more acreage to raise livestock. By not supporting the meat industry directly reduces the demand to pillage these irreplaceable treasures of nature. Since the forest land “filters” our air supply and contains botanical sources for new medicines, this destruction is irreversible.</p>
