									<p style="text-align: justify;">“Why should one be vegetarian? Because the God inside us wants it”, says&nbsp;Saint Dr. Gurmeet Ram Rahim Singh Ji Insan, the present Guru Ji of Dera Sacha Sauda.<br>
										One of the foremost principles of Dera Sacha Sauda stresses upon vegetarianism. Our religions have supported vegetarianism since ages. And now researches have also shown that human beings are vegetarian by nature. There are a number of evidences that reinforce people to be vegetarian; be it related to health, environment or ethics. The following sections share some of the facts beneficial for a healthy and happy living.</p>
									<div class="Htitle" style="text-align: justify;">
										<h2>Health:</h2>
									</div>
									<p style="text-align: justify;">Studies of human evolution have shown that our ancestors were vegetarian by nature. The structure of the human body is not suited for eating meat.&nbsp;
										<span style="font-size: 10.0pt; font-family: 'Helvetica','sans-serif'; color: #3c3c3c;">Dr. G. S. Huntingen of Columbia University, in an essay, pointed out that carnivores have shorter intestines. Their large intestine is characteristically very straight and smooth. In contrast, vegetarian animals have both a long small intestine and a long large intestine.</span> Because of the low fiber content and high protein density of meat, the intestines do not require a long time to absorb nutrients; thus, the intestines of carnivores are shorter in length than those of vegetarian animals.&nbsp;
										<span style="font-size: 10.0pt; font-family: 'Helvetica','sans-serif'; color: #3c3c3c;">Humans, like other naturally vegetarian animals, also have longer intestines. Together, our intestines are approximately twenty-eight feet (eight and a half meters) in length.</span>The small intestine is folded back on itself many times, and its walls are convoluted, not smooth. Because they are longer than those found in carnivores, the meat we eat stays in our intestines for a longer period of time. Consequently, the meat can putrefy and create toxins. These toxins have been implicated as a cause of colon cancer, and they also increase the burden on the liver, which has the function of getting rid of toxins. This can cause cirrhosis and even cancer of the liver.eat contains a lot of urokinase protein and urea, which add to the burden on the kidneys, and can destroy kidney function. There are fourteen grams of urokinase protein in every pound of steak. If living cells are put into liquid urokinase protein, their metabolic function will degenerate. Furthermore, meat lacks cellulose or fiber, and lack of fiber can easily create constipation. It is known that constipation can cause rectal cancer or piles.<br>
										The cholesterol and saturated fats in flesh also create cardiovascular disorders.<br>
										Cancer is the second leading cause of death. Experiments indicate that the burning and roasting of flesh creates a chemical element (Methylcholanthrene) which is a powerful carcinogen. Mice given this chemical develop cancers, such as bone tumors, cancer of the blood, cancer of the stomach, etc.<br>
										There is yet another concern. Antibiotics as well as other drugs including steroids and growth hormones are either added to animal feed or injected directly into the animals. It has been reported that people eating these animals will absorb these drugs into their bodies. There is a possibility that antibiotics in meat are diminishing the effectiveness of antibiotics for human use.<br>
										There are some people who consider the vegetarian diet not sufficiently nourishing.Renowned scientist working at scientific and industrial research council in national science institution, Bangalore, Dr. Keti has reached at the conclusion that vegetarian food is a complete food. A proper mix of vegetarian food items ,maintaining the nutrition at the time of cooking, eating boiled food, properly drying, sprouts and fermentation can increase the food quality. Milk and milk products have sufficient amount of protein, calcium and vitamins. The toned milk also contains 38 per cent of high protein. There is no nutritional element, which is present in meat and eggs and not available in milk. This is beneficial for the heart patients as well because it reduces the level of cholesterol in the blood; it is the antidote to several poisons.<br>
										The vegetarian food is rich in fiber whereas the non-vegetarian food is deficient in this element. These fibers are helpful in providing minerals and vitamins to the body. These avoid constipation and related diseases like piles, hernia, appendicitis, hepatitis, divertica, and varicose veins. These fibers are important because they protect us from stones and heart diseases by mutual action of cholesterol and bilirubin.<br>
										Many people have the idea that animal protein is ‘superior’ to plant protein because the former is considered a complete protein, and the latter is incomplete. The truth is that some plant proteins are complete, and that food combining can create complete proteins out of several incomplete protein foods.<br>
										It is often falsely believed that meat eaters are stronger than vegetarians, but an experiment conducted by
										<strong>Professor Irving Fisher</strong> of Yale University on 32 vegetarians and 15 meat-eaters showed that vegetarians had more endurance than meat eaters. He had people hold out their arms for as long as possible. The outcome from the test was very clear. Among the 15 meat-eaters, only two persons could hold out their arms for fifteen to thirty minutes; however, among the 32 vegetarians, 22 persons held out their arms for fifteen to thirty minutes, 15 persons for over thirty minutes, 9 persons for over one hour, 4 persons for over two hours, and one vegetarian held his arms out for over three hours.<br>
										Many long distance track athletes keep a vegetarian diet for the time preceding competitions.
										<strong>Dr. Barbara More</strong>, an expert in vegetarian therapy, completed a one hundred and ten mile race in twenty-seven hours and thirty minutes. A woman of fifty-six years of age, she broke all the records held by young men. “I want to be an example to show that people who take a whole vegetarian diet will enjoy a strong body, a clear mind, and a purified life.”<br>
										Does the vegetarian get enough protein in his diet?
										<span style="font-size: 10.0pt; font-family: 'Helvetica','sans-serif'; color: #3c3c3c;">The World Health Organization recommends that 4.5% of daily calories should be derived from protein. Wheat has 17% of it’s calories as protein, broccoli has 45% and rice has 8%. It is very easy to have a protein rich diet without eating meat. With the additional benefit of avoiding many diseases caused by high fat diets such as heart diseases and many cancers, vegetarianism is clearly the superior choice.</span>
										<span style="font-size: 10.0pt; font-family: 'Helvetica','sans-serif'; color: #3c3c3c;">The relationship between consumption of meat or other animal source foods containing high levels of saturated fats, and heart disease, breast cancer, colon cancer, strokes has been proven.</span>Other diseases which are often prevented and sometimes cured by a low fat vegetarian diet include: kidney stones, prostate cancer, diabetes, peptic ulcers, gallstones, irritable bowel syndrome, arthritis, gum disease, acne, pancreatic cancer, stomach cancer, hypoglycemia, constipation, diverticulosis, hypertension, osteoporosis, ovarian cancer, hemorrhoids, obesity, and asthma.</p>
